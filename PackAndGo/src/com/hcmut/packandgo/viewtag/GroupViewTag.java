package com.hcmut.packandgo.viewtag;

import android.widget.ImageView;
import android.widget.TextView;

public class GroupViewTag {

	private TextView groupnameView;
	private ImageView avatarView;
	
	public TextView getGroupnameView() {
		return groupnameView;
	}
	public void setGroupnameView(TextView groupnameView) {
		this.groupnameView = groupnameView;
	}
	public ImageView getAvatarView() {
		return avatarView;
	}
	public void setAvatarView(ImageView avatarView) {
		this.avatarView = avatarView;
	}
}
